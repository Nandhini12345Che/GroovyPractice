class DataType {
   static void main(String[] args) {
      //int datatype
      int a= 10;    
      //long datatype
      long b = 500L;  
      //floating point datatype
      float c = 60.09f;   
      // double datatype
      double d = 70.0e40;
      // BigInteger datatype
      BigInteger e = 1.5g;
      //BigDecimal datatype
      BigDecimal f= 10.5g;      

      println('Interger value :' +a);
      println('Long Value :' +b);
      println('Float Value' +c);
      println('Double Value' +d);
      println('BigInteger Value' +e);
      println('BigDecimal Value' +f);
   }
}
